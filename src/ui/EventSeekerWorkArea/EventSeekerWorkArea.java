    private void btnPostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPostActionPerformed
        // TODO add your handling code here:
        if(txtPosition.getText().isEmpty() || txtAge.getText().isEmpty() || txtSport.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill the Empty fields");
        }else{

               String Age ="^[0-9]*$";
               String alphabetsOnly = "^[a-zA-Z]*$";
                Pattern p = Pattern.compile(Age);
                Pattern a = Pattern.compile(alphabetsOnly);
                Matcher match= p.matcher(txtAge.getText());
                Matcher match2= a.matcher(txtCountry.getText());
                Matcher match3= a.matcher(txtPosition.getText());
                Matcher match4= a.matcher(txtSport.getText());
                try {

                if(!match.matches()){
                    ageValidation.setText("Invalid input! Enter numerical values only"); 
                    return;
                }
                else {
                    ageValidation.setText("");
                    if(Integer.parseInt(txtAge.getText())< 0 ||Integer.parseInt(txtAge.getText()) > 200){
                        ageValidation.setText("Enter valid age");
                        return;
                    } 
                }
                if(!match2.matches()){
                    cValidation.setText("Invalid input. Only alphabets allowed"); 
                    return;
                }
                else{
                    cValidation.setText("");
                }
                if(!match3.matches()){
                    pValidation.setText("Invalid input. Only alphabets allowed"); 
                    return;
                }
                else{
                    pValidation.setText("");
                }
                if(!match4.matches()){
                    sValidation.setText("Invalid input. Only alphabets allowed"); 
                    return;
                }
                else{
                    sValidation.setText("");
                }
    
        String subject = txtSport.getText();
        String PoString = txtPosition.getText();
        String age = txtAge.getText();
        String location = txtCountry.getText();
        
        Date d = new Date();
        SimpleDateFormat s = new SimpleDateFormat("MM/dd/YY");
        s.format(d);
        if(subject.equals("") || subject.isEmpty() && PoString.equals("") || PoString.isEmpty() && age.equals("")|| age.isEmpty() && location.equals("")|| location.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter valid request");
            return;
        }
        
        PlayerWorkRequest req = new PlayerWorkRequest();
        req.setSubject(subject);
        req.setAge(Integer.parseInt(age));
        req.setCountry(location);
        req.setPosition(PoString);
        req.setStatus("Requested");
        req.setSender(account);
        req.setRequestDate(d);
        organization.getOrgWorkQueue().getWorkRequestList().add(req);
        account.getWorkQueue().getWorkRequestList().add(req);
        system.getOrgWorkQueue().getWorkRequestList().add(req);
        
        populateTableWorkQueue();
        
        txtPosition.setText("");
        txtAge.setText("");
        txtSport.setText("");
        txtCountry.setText("");
        }
    catch (NumberFormatException e) {
       
    }
        JOptionPane.showMessageDialog(null, "Player created Successfully");
                return;
        }
    }//GEN-LAST:event_btnPostActionPerformed

